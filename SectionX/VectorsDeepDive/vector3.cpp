#include <iostream>
#include <vector>

using namespace std;

int main()
{
    vector <int> v;

    for(int x =1; x <= 30; x++)
    {
        v.push_back(x);
    }

    cout << "Vector size is: " << v.size() << endl;
    cout << "\nCapacity of Vector is: " << v.capacity() << endl;

    cout << "\nMaximum size of vector is : "<< v.max_size() <<endl;

    v.resize(5);
    cout << "\nVector size after resizing is: " << v.size() << endl;

    if(v.empty() == false)
    {
        cout << "\nVector is not empty" << endl;
    } else {
        cout << "Vector is empty";
    }


    return 0;

}